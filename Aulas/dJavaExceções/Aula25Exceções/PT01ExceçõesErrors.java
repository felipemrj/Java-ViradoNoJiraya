package dJavaExceções.Aula25Exceções;

public class PT01ExceçõesErrors {
    /*
    Exceções representam situações anormais que podem ocorrer durante a execução de um programa.
    Existem diversos tipos de exceções: algumas estão sob nosso controle, como impedir que um atributo receba
    valores negativos, permitindo que façamos tratamentos específicos. Outras estão fora do nosso controle,
    como falhas ao tentar acessar um arquivo inexistente ou sem permissão.
    O Java oferece várias ferramentas para lidar com esses fluxos anormais.

    No Java, todas as exceções são objetos da classe Throwable e se dividem em dois tipos principais:
     - Error
     - Exception

    Errors indicam problemas graves dos quais o programa normalmente não consegue se recuperar,
    como falta de memória (OutOfMemoryError) ou estouro de pilha (StackOverflowError).
    Nessas situações, geralmente é necessário interromper o programa, corrigir o problema e reiniciá-lo.
     */
}
