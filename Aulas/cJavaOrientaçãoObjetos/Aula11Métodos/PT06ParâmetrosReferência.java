package cJavaOrientaçãoObjetos.Aula11Métodos;

public class PT06ParâmetrosReferência {
/* Em Java, quando passamos um tipo de referência (como um objeto) como parâmetro para um método,
 o que é passado é uma cópia da referência(ou seja o local onde o objeto está na memória) para o objeto na memória, e não o próprio objeto.
 Isso significa que alterações feitas nas propriedades do objeto dentro do método afetam o objeto original fora dele.
 Por isso, métodos podem modificar o estado de objetos recebidos como parâmetro, diferente dos tipos primitivos.

Boas práticas: não altere dados de umo objeto dentro de um método! */
}
