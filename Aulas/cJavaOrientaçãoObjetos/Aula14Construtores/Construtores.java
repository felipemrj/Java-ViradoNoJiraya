package cJavaOrientaçãoObjetos.Aula14Construtores;

public class Construtores {
    /* Construtores são especiais em Java: eles servem para inicializar objetos quando são criados.
    Diferente dos métodos, construtores não têm tipo de retorno e seu nome é igual ao da classe.
    Se você não criar um construtor, o Java gera automaticamente um construtor padrão sem parâmetros.
    Além disso, é possível ter vários construtores na mesma classe, cada um com diferentes parâmetros,
    isso é chamado de sobrecarga de construtores, permitindo criar objetos de formas variadas conforme a necessidade.
    A sintaxe para chamar um construtor dentro de outro construtor é this(parametros);
    e ele sempre precisa ser a primeira linha do código*/
}
