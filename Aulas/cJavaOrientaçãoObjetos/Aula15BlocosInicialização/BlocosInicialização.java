package cJavaOrientaçãoObjetos.Aula15BlocosInicialização;

public class BlocosInicialização {
    /* Bloco de inicialização é um bloco de código dentro da classe que sempre será executado ao instanciar um objeto, ele sempre será
    executado antes de qualquer método da classe independente da ordem do código

    Blocos de inicialização em Java são úteis para garantir que certas operações sejam realizadas sempre que um objeto é criado,
    independentemente de qual construtor seja chamado. Eles são executados antes do construtor e podem ser usados para inicializar
    variáveis de instância, abrir conexões, carregar configurações ou executar qualquer lógica que precise ocorrer na criação do objeto.
    Caso haja herança, os blocos de inicialização da superclasse são executados antes dos blocos da subclasse.
    Além disso, é possível ter múltiplos blocos de inicialização em uma mesma classe, e eles serão executados na ordem em que aparecem no código.
    Isso ajuda a evitar duplicação de código entre construtores e facilita a manutenção.

    Dica: Ao chamarmos um get que retorna uma lista, podemos usar o atalho ALT ENTER
    para criar o for mais facilmente */
}
