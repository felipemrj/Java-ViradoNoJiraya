public class PT01Herança {
    /*
    Herança é um conceito fundamental da programação orientada a objetos que permite que uma classe (subclasse) herde atributos e métodos
    de outra classe (superclasse). Ela representa uma relação "é um", onde a subclasse é um tipo especializado da superclasse.
    Por exemplo, se Animal é uma superclasse, Cachorro pode ser uma subclasse, pois "Cachorro é um Animal".

    A herança facilita o reaproveitamento de código, a organização hierárquica e a especialização de comportamentos.
    É aplicável quando diferentes classes compartilham características comuns, mas também possuem particularidades.
    Assim, evita duplicação e torna o sistema mais flexível e fácil de manter.
     */
}
