package cJavaOrientaçãoObjetos.Aula18Herança;

public class PT02HerançaSuper {
    /*
    O super é uma palavra-chave em Java usada para acessar membros (atributos e métodos) da superclasse a partir da subclasse. Ela é útil para:
    Chamar o construtor da superclasse: super()
    Acessar métodos da superclasse: super.metodo()
    Acessar atributos da superclasse: super.atributo

    public class Pessoa {
        private String nome;
        public void imprime() {
            System.out.println("Nome: " + nome);
            }
        }

    public class Funcionario extends Pessoa {
        private double salario;
        @Override
        public void imprime() {
            super.imprime(); // Chama o método imprime da superclasse
            System.out.println("Salário: " + salario);
            }
        }

     */
}
