package cJavaOrientaçãoObjetos.Aula20ModificadorFinal;

public class PT01ModificadorFinalPrimitivo {
    /*
    Constantes em Java são valores que não podem ser alterados após sua definição. Para criar uma constante, usa-se o modificador final,
    que impede a modificação do valor da variável.

    A convenção para nomear variáveis constantes é usar letras maiúsculas e, se necessário,
    separar palavras com o caractere de sublinhado (_). Exemplo: VELOCIDADE_LIMITE.

    O modificador final em variáveis de tipos primitivos faz com que o valor atribuído não possa ser alterado após a inicialização.
    Ou seja, uma vez definido, o valor permanece constante durante toda a execução do programa. Qualquer tentativa de modificar o
    valor resultará em erro de compilação.

    Exemplo:
    public class ExemploFinalPrimitivo {
        public static final int VELOCIDADE_LIMITE = 120; // constante de tipo primitivo
            public static void main(String[] args) {
                // VELOCIDADE_LIMITE = 130; // Erro: não é possível alterar uma variável final
                System.out.println(VELOCIDADE_LIMITE);
            }
    }
     */
}
